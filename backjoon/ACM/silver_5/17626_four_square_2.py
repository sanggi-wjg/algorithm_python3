def solution(n):
    print('N:', n)
    for i in range(1, 50000):
        x = i ** 2
        if x == n:
            return 0

        if x > n:
            # print('i-1', i-1)
            return n - ((i - 1) ** 2)


N = int(input())
count = 1

while True:
    N = solution(N)
    if N == 0:
        break
    count += 1

print(count)
