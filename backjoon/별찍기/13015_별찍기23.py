N = int(input())

for i in range(N):
    if i == 0:
        print(('*' * N) + (' ' * (N - 1)) + (' ' * (N - 2)) + ('*' * N))
    elif i == N - 1:
        print((' ' * (N - 1)) + '*' + (' ' * (N - 2)) + '*' + (' ' * (N - 2)) + '*')
    else:
        print((' ' * i) + '*' + (' ' * (N - 2)) + '*' + (' ' * (N - i - 1)) + (' ' * (N - i - 2)) + '*' + (' ' * (N - 2)) + '*')

for i in range(N, 1, -1):
    if i == 2:
        print(('*' * N) + (' ' * (N - 1)) + (' ' * (N - 2)) + ('*' * N))
    else:
        print((' ' * (i - 2)) + '*' + (' ' * (N - 2)) + '*' + (' ' * (N - i + 1)) + (' ' * (N - i)) + '*' + (' ' * (N - 2)) + '*')

